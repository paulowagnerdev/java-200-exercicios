package capitulo_01;

public class Exercices05 {

	public static void main(String[] args) {

		/*
		 * Exercício 5: Operações Aritméticas Desenvolva um programa que declare duas
		 * variáveis int e realize as operações de soma, subtração, multiplicação,
		 * divisão e módulo entre elas. Exiba os resultados de cada operação.
		 */
		
		int a = 120;
		int b = 50;
		
		System.out.println("Soma = " + (a + b));
		System.out.println("Subtração = " + (a - b));
		System.out.println("Multiplicação = " + a * b);
		System.out.println("Divisão = " + a / b);
		System.out.println("Módulo = " + a % b);
	}

}
